# Write a program to calculate the area of circle using formula π(r^2)

r = float(input("Enter the value of radius : "))

Area_of_circle = 3.14 *(r*2) # π = 3.14

print("Area of circle = ",Area_of_circle)

# Write a program to calculate the area of rectangle using formula (A*B)

A = float(input("Enter the length : "))
B = float(input("Enter the breadth : "))

Area_of_rectangle = A*B

print("The area of rectangle = ",Area_of_rectangle)
